package com.charlie.ev3;

/**
 * EV3主机的按钮。
 * @author Charlie Jiang
 */
public enum BrickButtons {
    /**
     * 没有按钮。
     */
    NONE,
    /**
     * 上按钮。
     */
    UP,
    /**
     * 确定（中）按钮。
     */
    ENTER,
    /**
     * 下按钮。
     */
    DOWN,
    /**
     * 右按钮。
     */
    RIGHT,
    /**
     * 左按钮。
     */
    LEFT,
    /**
     * 返回按钮。
     */
    ESCAPE,
    /**
     * 任意按钮。
     */
    ANY
}
