package com.charlie.ev3;

/**
 * 从颜色传感器返回的颜色值。
 * @author Charlie Jiang
 */
public enum ColorSensorColor {
    /**
     * 透明。
     */
    TRANSPARENT,
    /**
     * 黑色。
     */
    BLACK,
    /**
     * 蓝色。
     */
    BLUE,
    /**
     * 绿色。
     */
    GREEN,
    /**
     * 黄色。
     */
    YELLOW,
    /**
     * 红色。
     */
    RED,
    /**
     * 白色。
     */
    WHITE,
    /**
     * 棕色。
     */
    BROWN
}
