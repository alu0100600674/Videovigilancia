package com.charlie.ev3;

/**
 * NXT颜色值。
 * @author Charlie Jiang
 */
public enum NXTColorMode {
    /**
     * 反射值。
     */
    REFLECTIVE,
    /**
     * 环境光。
     */
    AMBIENT,
    /**
     * 颜色。
     */
    COLOR,
    /**
     * 红色返回值。
     */
    GREEN,
    /**
     * 蓝色返回值。
     */
    BLUE,
    /**
     * 原始传感器值。
     */
    RAW
}
